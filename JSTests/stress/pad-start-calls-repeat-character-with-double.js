function logLinesWithContext(n, context) {
    let start = n - context;
    let end = n + context;
    for (let x = start; x <= end; ++x) {
        let number = x.toString().padStart(3);
        if (parseInt(number) !== x)
            throw new Error("Bad result from pad start: " + number);
    }
}
