const matchSymbols = buildString({
  loneCodePoints: [],
  ranges: [
    [0x013000, 0x013455]
  ]
});
testPropertyEscapes(
  /^\p{Script=Egyptian_Hieroglyphs}+$/u,
  matchSymbols,
  "\\p{Script=Egyptian_Hieroglyphs}"
);
testPropertyEscapes(
  /^\p{Script=Egyp}+$/u,
  matchSymbols,
  "\\p{Script=Egyp}"
);
testPropertyEscapes(
  /^\p{sc=Egyptian_Hieroglyphs}+$/u,
  matchSymbols,
  "\\p{sc=Egyptian_Hieroglyphs}"
);
testPropertyEscapes(
  /^\p{sc=Egyp}+$/u,
  matchSymbols,
  "\\p{sc=Egyp}"
);

const nonMatchSymbols = buildString({
  loneCodePoints: [],
  ranges: [
    [0x00DC00, 0x00DFFF],
    [0x000000, 0x00DBFF],
    [0x00E000, 0x012FFF],
    [0x013456, 0x10FFFF]
  ]
});
testPropertyEscapes(
  /^\P{Script=Egyptian_Hieroglyphs}+$/u,
  nonMatchSymbols,
  "\\P{Script=Egyptian_Hieroglyphs}"
);
testPropertyEscapes(
  /^\P{Script=Egyp}+$/u,
  nonMatchSymbols,
  "\\P{Script=Egyp}"
);
testPropertyEscapes(
  /^\P{sc=Egyptian_Hieroglyphs}+$/u,
  nonMatchSymbols,
  "\\P{sc=Egyptian_Hieroglyphs}"
);
testPropertyEscapes(
  /^\P{sc=Egyp}+$/u,
  nonMatchSymbols,
  "\\P{sc=Egyp}"
);
