(options, args) = parser.parse_args(sys.argv[0:])
if len(args) != 3:
    parser.error(parser.usage)

jsonPath = args[1]
jsonDirectory = os.path.dirname(os.path.abspath(jsonPath))
headerFile = open(args[2], "w")

opcodes = simdOpcodes(jsonDirectory)
contents = ""
for key in opcodes:
    op = opcodes[key]
    if op["is_simd_special_op"]:
        continue

    hasImm = len(op["immediate"]) != 0
    assert(len(op["parameter"]))
    assert(len(op["return"]) == 1)

    s = "\n"
    s = s + functionGeneratorDecl(key, op, "LLIntGenerator")
    s = s + f"    result = push(Types::{cppType(op['return'][0])});\n"
    s = s + "    " + op["wasmOp"] + "::emit(this, result, "
    if hasImm:
        s = s + "imm, "

    i = 0
    for x in op["parameter"]:
        s = s + "arg" + str(i)
        i = i + 1
        if i < len(op["parameter"]):
            s = s + ", "

    s = s + ");"
    s = s + "\n    return { };\n}"

    contents = contents + s


